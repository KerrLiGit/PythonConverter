class Length:
    def meters_to_inch(self, a):
        return a * 39.37

    def kilometers_to_mile(self, a):
        return a / 1.609

    def meters_to_yard(self, a):
        return a * 1.094

    def meters_to_foots(self, a):
        return a * 3.281

    def kilometers_to_sea_mile(self, a):
        return a / 1.852
